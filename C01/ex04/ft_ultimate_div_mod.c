#include <unistd.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int		kln;
	int		blm;

	if (*b != 0)
	{
		blm = *a / *b;
		kln = *a % *b;
		*a = blm;
		*b = kln;
	}
}
