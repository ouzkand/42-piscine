#include<unistd.h>

void	print(char a)
{
	write(1, &a, 1);
}

void	ft_print_comb2(void)
{
	int		x;
	int		y;

	x = 0;
	while (x <= 98)
	{
		y = x + 1;
		while (y <= 99)
		{
			print(x / 10 + '0');
			print(x % 10 + '0');
			write(1, " ", 1);
			print(y / 10 + '0');
			print(y % 10 + '0');
			if (x != 98)
			{
				write(1, ", ", 2);
			}
			y++;
		}
		x++;
	}
}
