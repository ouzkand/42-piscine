#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	int		i;
	int		r;

	r = 1;
	i = 0;
	while (str[i])
	{
		if (str[i] >= 32 && str[i] <= 126)
		{
			r = 1;
		}
		else
		{
			r = 0;
			break ;
		}
		i++;
	}
	return (r);
}
